void Conv2Post(const GFSymbol *A, GFSymbol *P)
{
	P[0] = A[0] ^ A[1];
	P[1] = A[0] ^ A[2];
};  // 2XOR

void Conv2Pre(const GFSymbol *A, GFSymbol *P)
{
	P[1] = A[1];
	P[2] = A[0];
	P[0] = A[0] ^ A[1];
};  // 1XOR


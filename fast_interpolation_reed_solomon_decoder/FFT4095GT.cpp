void Conv2Pre(const GFSymbol *A, GFSymbol *P)
{
  P[0]=A[0]^A[1];
  P[1]=A[1];
  P[2]=A[0];

}

void Conv2Post(const GFSymbol *A, GFSymbol *P)
{
	P[0] = A[0] ^ A[1];
        P[1]=  A[0] ^ A[2];
};

void Conv2_Apply(const GFSymbol *pA, GFSymbol *pB, const galois_field &GF)
{
  GFSymbol T[3];
  Conv2Pre(pA,T);
T[0]=GF.multiply(T[0],71);
T[1]=GF.multiply(T[1],1);
T[2]=GF.multiply(T[2],1);
  Conv2Post(T,pB);

}

void Conv3Post(const GFSymbol *A, GFSymbol *P)
{
	P[0] = A[0] ^ A[2];
        P[1]=  P[0] ^ A[1];
        P[0]^= A[3];
	P[2] = A[0] ^ A[1] ^ A[3];
};//4XOR

void Conv3Pre(const GFSymbol *A, GFSymbol *P)
{
	P[2] = A[0]^A[1];
	P[0] = P[2]^A[2];
	P[1] = A[1] ^ A[2];
	P[3] = A[0] ^ A[2];
};//4XOR




